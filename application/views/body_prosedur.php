				<p>Jika penuh, Staff Bagian Human Investment akan megarahkan siswa untuk mengganti waktu PKL, menawarkan Siswa/Mahasiswa PKL ke Divisi/Bagian lain  atau menyarankan pindah ke Perusahaan lain.</p>
